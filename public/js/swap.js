function swap(a,b) {
    document.getElementById(a).style.display = 'block';
    document.getElementById(b).style.display = 'none';
}
function swapOne(a,b,c) {
    document.getElementById(a).style.display = 'block';
    document.getElementById(b).style.display = 'none';
    document.getElementById(c).style.display = 'none';
}